# Integer variables
num1 = 10
num2 = 5

# Integer Division
result1 = num1 // num2
print("Integer Division:", result1)

# Modulus (Remainder)
result2 = num1 % num2
print("Modulus (Remainder):", result2)

# Absolute Value
result3 = abs(-7)
print("Absolute Value:", result3)

result4 = num1 * num2
print("multiplication:", result4)

result5 = num1 - num2
print("substraction:", result5)

result6 = abs(-55)
print("abbstraction:", result6)
